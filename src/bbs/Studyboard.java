package bbs;

public class Studyboard {
	private int studyboardID;
	private String studyboardTitle;
	private String userID;
	private String studyboardDate;
	private String studyboardContent;
	private int studyboardAvailable;
	public int getStudyboardID() {
		return studyboardID;
	}
	public void setStudyboardID(int studyboardID) {
		this.studyboardID = studyboardID;
	}
	public String getStudyboardTitle() {
		return studyboardTitle;
	}
	public void setStudyboardTitle(String studyboardTitle) {
		this.studyboardTitle = studyboardTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getStudyboardDate() {
		return studyboardDate;
	}
	public void setStudyboardDate(String studyboardDate) {
		this.studyboardDate = studyboardDate;
	}
	public String getStudyboardContent() {
		return studyboardContent;
	}
	public void setStudyboardContent(String studyboardContent) {
		this.studyboardContent = studyboardContent;
	}
	public int getStudyboardAvailable() {
		return studyboardAvailable;
	}
	public void setStudyboardAvailable(int studyboardAvailable) {
		this.studyboardAvailable = studyboardAvailable;
	}
	
	
}
